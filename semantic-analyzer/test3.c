// 測試  if, elseif 跟 else
#include <stdio.h>
#include <stdlib.h>
#include <math.h>
// 測錯可以任意刪除一個括弧，或者把 function return 拔掉。
int main()
{
    int x;
    if(x == 0)
    {
        // x is zero        
    }
    else if(x == 1)
    {
        // x is one
    }
    else if(x == 2)
    {
        // x is two
    }
    else
    {
        // x is three or four
    }
    return 0;
}